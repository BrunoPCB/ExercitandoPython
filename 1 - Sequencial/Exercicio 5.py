"""
5 - Faça um Programa que converta metros para centímetros.
"""
# Informando um valor de metros
numero_metros = float(input("Informe um valor em metros: "))

# Calculando a converção de metros -> centímetros
numero_centimetros = numero_metros * 100

# Mostrando ao usuário a conversão de metro -> centímetros
print("Conversão de Metros -> Centímetros")
print(f"{numero_metros} -> {numero_centimetros}")
